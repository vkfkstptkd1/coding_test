# 두 자연수 a,b에 대하여 (a > b) a를 b로 나눈 나머지를 R이라고 하면
# a, b 에 대한 최대 공약수는 B와 R의 최대 공약수
# 재귀함수는 반복문을 이용하여 동일하게 기능을 구현할 수 있다. 
# 컴퓨터가 함수를 연속적으로 호출하면 컴퓨터 메모리 내부의 스택 프레임이 쌓인다.
# 그래서 스택을 사용해야할 때 구현상 스택 라이브러리 대신 재귀함수를 이용하는 경우가 많다. 
def gcd(a,b):
    R = a % b
    if R != 0:
        return gcd(b,R)
    else:
        return b
 
 
print(gcd(192,162))